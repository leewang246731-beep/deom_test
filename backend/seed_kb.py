def clear_kb(db, merchant_id: int):
    """清空指定商户的知识库。"""
    chunk_count = db.query(KbChunk).filter(KbChunk.merchant_id == merchant_id).delete(synchronize_session=False)
    doc_count = db.query(KbDocument).filter(KbDocument.merchant_id == merchant_id).delete(synchronize_session=False)
    db.flush()
    return doc_count, chunk_count


def seed_kb_for_merchant(db, merchant_id: int, merchant_name: str, docs: list[dict]) -> dict:
    """为单个商户创建知识库文档。"""
    created = 0
    for doc_data in docs:
        # 幂等：检查标题是否已存在
        exist = db.query(KbDocument).filter(
            KbDocument.merchant_id == merchant_id,
            KbDocument.title == doc_data["title"],
        ).first()
        if exist:
            continue
        doc = KbDocument(
            merchant_id=merchant_id,
            title=doc_data["title"],
            content=doc_data["content"],
            source_type=doc_data.get("source_type", "manual"),
            status="pending",
        )
        db.add(doc)
        created += 1
    db.commit()
    return {"created": created}


def vectorize_documents(db, merchant_id: int) -> dict:
    """对 pending 状态的文档执行向量化。"""
    from app.kb.processor import process_document
    from app.services.embedding import embed_texts

    docs = db.query(KbDocument).filter(
        KbDocument.merchant_id == merchant_id,
        KbDocument.status == "pending",
    ).all()

    total_chunks = 0
    for doc in docs:
        try:
            result = process_document(db, doc.id, merchant_id, embed_texts)
            total_chunks += result.chunk_count
        except Exception as e:
            print(f"  [WARN] 向量化失败 doc={doc.id} {doc.title}: {e}")

    return {"documents": len(docs), "chunks": total_chunks}


def main():
    Base.metadata.create_all(bind=engine, checkfirst=True)
    db = SessionLocal()

    clear_only = "--clear-only" in sys.argv

    try:
        merchants = db.query(Merchant).filter(Merchant.status == 1).all()
        print(f"找到 {len(merchants)} 个活跃商户")
        print("=" * 50)

        total_docs = 0
        total_chunks = 0

        for m in merchants:
            # 清空旧知识库
            old_docs, old_chunks = clear_kb(db, m.id)
            print(f"\n[{m.name}] 清空旧数据: {old_docs} 文档 / {old_chunks} 切片")

            if clear_only:
                continue

            # 合并通用文档 + 商户专属文档
            docs = UNIVERSAL_DOCS + MERCHANT_DOCS.get(m.name, [])
            if not docs:
                print(f"  无匹配文档模板，跳过")
                continue

            # 创建文档
            result = seed_kb_for_merchant(db, m.id, m.name, docs)
            print(f"  创建文档: {result['created']} 篇")

            # 向量化
            vec_result = vectorize_documents(db, m.id)
            print(f"  向量化: {vec_result['documents']} 文档 → {vec_result['chunks']} 切片")

            total_docs += result["created"]
            total_chunks += vec_result["chunks"]

        if clear_only:
            print(f"\n[DONE] 已清空所有商户知识库")
        else:
            print(f"\n[DONE] 总计: {total_docs} 篇文档 / {total_chunks} 个向量切片")

    except Exception as e:
        db.rollback()
        print(f"[ERROR] {e}")
        raise
    finally:
        db.close()


if __name__ == "__main__":
    main()
